#include "libft.h"

int	ft_strcmp(const char *p1, const char *p2)
{
	int i;

	i = 0;
	while (p1[i] != '\0' && p1[i] == p2[i])
		i++;
	return ((unsigned char)(p1[i]) - (unsigned char)(p2[i]));
}
