char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int l;

	l = -1;
	i = -1;
	while (dest[++i] != '\0')
		;
	while (src[++l] != '\0')
		dest[i + l] = src[l];
	dest[i + l] = '\0';
	return (dest);
}
