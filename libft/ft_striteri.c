#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	char *s1;

	if (s != NULL && f != NULL)
	{
		s1 = s;
		while (*s1)
		{
			f((unsigned int)(s1 - s), s1);
			s1++;
		}
	}
}
